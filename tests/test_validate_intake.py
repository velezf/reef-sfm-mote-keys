def test_strict_mode_returns_nonzero_on_failure(edr_site, tmp_path):
    """--strict causes non-zero exit when overall_severity is fail."""
    out_dir = tmp_path / "out"
    out_dir.mkdir()

    # Write a TIFF with wrong software so software_lineage fails
