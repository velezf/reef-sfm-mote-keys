def test_size_mismatch_in_manifest(edr_site):
    """The manifest check captures when on-disk file size differs from manifest size."""
    # Write a manifest with wrong sizes
    manifest_path = edr_site["manifest"]
    names = edr_site["names"]
    with manifest_path.open("w") as fh:
        fh.write("url,name,size\n")
        for name in names:
            # Claim each file is 1 GB
            fh.write(f"https://example.usgs.gov/{name},{name},1073741824\n")
    # The inventory itself doesn't fail — the manifest check is surfaced in
    # extra_context by cmd_validate_intake.  Here we verify size_bytes is real.
    ids_records = load_ids_csv(edr_site["ids_csv"])
    inv = build_inventory(edr_site["site_dir"], ids_records=ids_records, use_exiftool=False)
    for rec in inv:
        assert rec.size_bytes < 1_073_741_824  # on-disk < claimed 1 GB


