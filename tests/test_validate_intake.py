_IDS_COLUMNS = [
    "id", "uuid", "image_id", "credit", "contact", "copyright",
    "usageterms", "imagedescription", "attribution_url",
    "event",           # always null
    "gpsareainformation", "gpsmapdatum",
    "pid",             # always null
    "dtoriginal", "dtmodified",
    "gpsdate",         # always null
    "gpstime",         # always null
    "lat", "latref", "lng", "lngref",
    "artist", "externalmetadata",
    "dtdigitized",     # always null
    "contributor",     # always null
    "preservedfilename", "cammake", "cammodel",
]

_NULL_COLS = {"event", "pid", "gpsdate", "gpstime", "dtdigitized", "contributor"}


def _csv_row(
    filename: str,
    image_id: int = 1,
    *,
    cammake: str = "Canon",
    cammodel: str = "Canon PowerShot S120",
    artist: str = EXPECTED_EXIF_ARTIST,
    copyright_: str = EXPECTED_EXIF_COPYRIGHT,
    dtoriginal: str = "2023-07-12 14:39:55+00:00",
    lat: float = 24.45913,
    lng: float = -81.84534,
) -> dict[str, Any]:
    row = {c: "" for c in _IDS_COLUMNS}
    row.update(
        id=image_id,
        uuid="32d24f1600254494b5722ea5d677d936",
        image_id=image_id,
        credit="U.S. Geological Survey",
        contact="gs-g-spcmsc_data_inquiries@usgs.gov",
        copyright=copyright_,
        usageterms="Public Domain usage terms",
        imagedescription="test image",
        attribution_url="https://doi.org/10.5066/P1WHKTRD",
        gpsareainformation="station coordinates obtained from handheld GPS",
        gpsmapdatum="EPSG:4326 (WGS 84)",
        dtoriginal=dtoriginal,
        dtmodified="2023-07-14 11:35:10+00:00",
        lat=lat,
        latref="N",
        lng=lng,
        lngref="W",
        artist=artist,
        externalmetadata="https://www1.usgs.gov/pir/api/identifiers/USGS:test",
        preservedfilename=filename,
        cammake=cammake,
        cammodel=cammodel,
    )
    # Null columns are empty strings — pandas will read them as NaN
    for col in _NULL_COLS:
        row[col] = ""
    return row


def _write_ids_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    with path.open("w", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=_IDS_COLUMNS)
        writer.writeheader()
        writer.writerows(rows)


