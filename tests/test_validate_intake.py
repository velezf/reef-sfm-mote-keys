def test_output_artifacts_written(edr_site, tmp_path):
    """validate-intake always writes inventory.json, qc_report.json, qc_report.md."""
    out_dir = tmp_path / "out"
    out_dir.mkdir()
    parser = build_parser()
    args = parser.parse_args([
        "validate-intake",
        str(edr_site["site_dir"]),
        "--ids-csv", str(edr_site["ids_csv"]),
        "--out-dir", str(out_dir),
    ])
    from reef_sfm_provenance.__main__ import cmd_validate_intake
    cmd_validate_intake(args)

    assert (out_dir / "inventory.json").exists()
    assert (out_dir / "qc_report.json").exists()
    assert (out_dir / "qc_report.md").exists()

    report = json.loads((out_dir / "qc_report.json").read_text())
    assert report["schema"] == "reef-sfm-provenance/intake_qc/v2"
    assert report["image_count"] == 3
    assert report["csv_join_count"] == 3
    assert "metadata_lineage" in report
    assert "DateTimeOriginal" in report["metadata_lineage"]["fields_absent_by_design"]["missing_exif_tags"]
