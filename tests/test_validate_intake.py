# ---------------------------------------------------------------------------
# EXIF tag IDs
# ---------------------------------------------------------------------------

TAG_MAKE = 271
TAG_MODEL = 272
TAG_ARTIST = 315
TAG_COPYRIGHT = 33432
TAG_SOFTWARE = 305
TAG_ORIENTATION = 274


# ---------------------------------------------------------------------------
# Synthetic TIFF helpers
# ---------------------------------------------------------------------------


def _write_photoshop_tiff(
    path: Path,
    *,
    width: int = 4000,
    height: int = 3000,
    software: str = "Adobe Photoshop 24.6 (Windows)",
    make: str = "Canon",
    model: str = "Canon PowerShot S120",
    artist: str = EXPECTED_EXIF_ARTIST,
    copyright_: str = EXPECTED_EXIF_COPYRIGHT,
    orientation: int = 1,
) -> None:
    """Write a tiny TIFF matching ADR-0009 Finding B.

    Deliberately omits ExposureTime, FNumber, ISOSpeedRatings, FocalLength,
    DateTimeOriginal — Photoshop's TIFF export drops the Exif sub-IFD.
    """
    info = TiffImagePlugin.ImageFileDirectory_v2()
    if make:
        info[TAG_MAKE] = make
    if model:
        info[TAG_MODEL] = model
    if artist:
        info[TAG_ARTIST] = artist
    if copyright_:
        info[TAG_COPYRIGHT] = copyright_
    if software:
        info[TAG_SOFTWARE] = software
    if orientation:
        info[TAG_ORIENTATION] = orientation

    img = Image.new("RGB", (width, height), (60, 90, 120))
    img.save(path, format="TIFF", tiffinfo=info, compression="tiff_lzw")


